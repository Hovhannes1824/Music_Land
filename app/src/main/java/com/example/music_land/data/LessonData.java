    public static List<Lesson> getLessonsForGenre(String genre) {
        return genreLessons.getOrDefault(genre, new ArrayList<>());
    }

    public static List<String> getAllGenres() {
        return new ArrayList<>(genreLessons.keySet());
    }

    public static List<Lesson> getAllLessons() {
        List<Lesson> allLessons = new ArrayList<>();
        // Используем HashSet для отслеживания уже добавленных названий
        HashSet<String> lessonTitles = new HashSet<>();
        
        for (String genre : genreLessons.keySet()) {
            for (Lesson lesson : getLessonsForGenre(genre)) {
                // Добавляем урок только если его название ещё не встречалось
                if (!lessonTitles.contains(lesson.getTitle())) {
                    allLessons.add(lesson);
                    lessonTitles.add(lesson.getTitle());
                }
            }
        }
        return allLessons;
    }
